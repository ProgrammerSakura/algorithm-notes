#include<stdio.h>

int main() {
    double I; //利润I
    double prize; //奖金
    //输入企业利润
    scanf("%lf", &I);
    if (I <= 100000)
    {
        /* code */
        prize = I * 0.1;
    } else if(100000 < I <= 200000) {
        prize = 100000 * 0.1 + (I - 100000) * 0.075;
    } else if(200000 < I <= 400000) {
        prize = ( 100000 * 0.1 + (I - 100000) * 0.075 ) + (I - 200000) * 0.05;
    } else if(400000 < I <= 600000) {
        prize = ( ( 100000 * 0.1 + (I - 100000) * 0.075 ) + (I - 200000) * 0.05 ) + (I - 400000) * 0.03;
    } else if(600000 < I <= 1000000) {
        prize = ( ( ( 100000 * 0.1 + (I - 100000) * 0.075 ) + (I - 200000) * 0.05 ) + (I - 400000) * 0.03 ) + (I - 600000) * 0.015;
    } else if(I > 1000000) {
        prize = ( ( ( ( 100000 * 0.1 + (I - 100000) * 0.075 ) + (I - 200000) * 0.05 ) + (I - 400000) * 0.03 ) + (I - 600000) * 0.015 ) + (I - 1000000) * 0.001;
    }
    printf("%.2f\n", prize);
}